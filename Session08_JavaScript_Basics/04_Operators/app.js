// Arithmetic operators
console.log(2 + 2); //4
console.log(2 - 3); //-1
console.log(2 * 3); //6
console.log(2 / 3); //0.666
console.log(2 % 3); //2
console.log(4 % 3); //1

//comparison operator
console.log(2 == 2); //true
console.log(2 == '2'); //true
console.log(2 === '2'); //false

console.log(2 != 2); //false
console.log(2 != '2'); //false
console.log(2 !== '2'); //true

//Data
console.log(typeof 2);
console.log(typeof '20');
console.log(typeof true);

var myVar;
console.log(typeof myVar);

myVar = null;
console.log(typeof myVar);

//Operators
console.log((2 < 5) && (5 > 8)); //AND
console.log((2 < 5) || (5 > 8)); //OR