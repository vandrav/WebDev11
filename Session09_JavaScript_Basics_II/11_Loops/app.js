// While loop

var n = 10;
var i = 0;
while (i <= n) {
    console.log(i);
    i++;
}

// DO WHILE -> vreau sa se execute ce e in structura macr odata orice s-ar intampla

var n = 10;
var i = 0;
do {
    console.log(i);
    i++;
} while (i <= n)


// FOR

var n = 10;
for (var i = 0; i <= n; i++) {
    console.log(i);
}